fn build_columns(column: &ColumnMembers, types_conversion: &Vec<&TypeMapper>) -> Option<String> {
    let mut column_ddl = String::new();
    column_ddl.push_str("\"");
    column_ddl.push_str(column.get_column_name());
    column_ddl.push_str("\"");
    column_ddl.push_str(" ");
    let empty_type = &&TypeMapper::empty_struct();
    let new_type = types_conversion
        .iter()
        .find(|type_origin| type_origin.get_type_origin().eq(column.get_data_type()))
        .unwrap_or_else(|| empty_type)
        .get_type_destiny();
    column_ddl.push_str(new_type);
    if column.get_lenght_field() > &0 {
        column_ddl.push_str("(");
        column_ddl.push_str(&column.get_lenght_field().to_string());
        column_ddl.push_str(") ");
    }
    if column.get_numeric_precision() > &0
        && column.get_numeric_scale() > &0
        && types_conversion.iter().any(|pred| {
            pred.get_type_origin().eq(column.get_data_type()) && *pred.get_is_precision()
        })
    {
        column_ddl.push_str("(");
        column_ddl.push_str(&column.get_numeric_precision().to_string());
        column_ddl.push_str(",");
        column_ddl.push_str(&column.get_numeric_scale().to_string());
        column_ddl.push_str(")");
    }
    column_ddl.push_str(" ");
    if !column.get_collation().is_empty() {
        column_ddl.push_str("COLLATE \"");
        column_ddl.push_str(column.get_collation());
        column_ddl.push_str("\"  ");
    }
    if *column.get_is_nullable() {
        column_ddl.push_str("NULL");
    } else {
        column_ddl.push_str("NOT NULL");
    }
    Some(column_ddl)
}

pub fn build_collation_mod(collations_coll: &Vec<Collations>) -> Option<Vec<String>> {
    let mut collations_ddl: Vec<String> = Vec::new();
    for collation in collations_coll {
        match collation.get_destiny_engine_ref() {
            VendorOptions::POSTGRES => {
                let mut collation_ddl = String::new();
                collation_ddl.push_str("CREATE COLLATION IF NOT EXISTS \"");
                let original_collation = match collation.get_collation_origin_ref() {
                    DBCollation::MSSQL(collation) => collation.as_str(),
                    _ => "",
                };
                collation_ddl.push_str(original_collation);
                collation_ddl.push_str("\" (");
                match collation.get_collation_destiny_ref() {
                    DBCollation::POSTGRES(collations) => {
                        collation_ddl.push_str("PROVIDER = '");
                        collation_ddl.push_str(collations.get_provider_as_ref());
                        collation_ddl.push_str("',");
                        collation_ddl.push_str(" LOCALE = '");
                        collation_ddl.push_str(collations.get_locale_as_ref());
                        collation_ddl.push_str("',");
                        collation_ddl.push_str(" DETERMINISTIC = ");
                        collation_ddl.push_str(&collations.get_deterministic_as_ref().to_string());
                        collation_ddl.push_str(");")
                    }
                    _ => {}
                }
                collations_ddl.push(collation_ddl);
            }
            _ => {} // other cases doesn't apply since here's just for MSSQL to PG
        }
    }
    Some(collations_ddl)
}

